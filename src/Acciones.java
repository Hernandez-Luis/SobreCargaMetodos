public class Acciones {
    public String operacion(String  a,String  b){
        return  a + b;
    }

    public int operacion(int a, int b){
        return a - b;
    }

    public Integer operacion(Integer c,Integer d){
        return c * d;
    }

    public float operacion(float a, float b){
        return a / b;
    }
}


